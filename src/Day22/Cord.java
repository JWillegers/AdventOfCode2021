package Day22;

public class Cord {
    protected int x;
    protected int y;
    protected int z;
    protected int xLine;
    protected int yLine;
    protected int zLine;

    protected Cord(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.xLine = 10;
        this.yLine = 10;
        this.zLine = 10;
    }
}
